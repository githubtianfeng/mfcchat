void MsgCallBack(DWORD id, void* param, int len)
{
	stMsgHead head;
	memcpy(&head, param, sizeof(head));
	switch (head.msgType)
	{
	case msg_UpdateFile:
	{
		if (sizeof(stMsgUpdata) != len)
		{
			return;
		}
		stMsgUpdata msg;
		memcpy(&msg, param, len);

		WriteUpdateFile(msg);
	}
		break;
	}
}

