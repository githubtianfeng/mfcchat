void CupdateClientDlg::UpdateFiles()
{
	//发个消息告诉服务器我要更新了
	stMsgUpdata msg;
	msg.curVersion = g_curVer;
	msg.head.msgType = msg_UpdateFile;
	SendToServer(&msg, sizeof(stMsgUpdata));
}

